package treino;

public class MediaAritmética {
	
	public static void main(String[] args) {
		  
		double n1 = 4.0;
		double n2 = 12.0;
		double n3 = 15.0;
		double res = 0.0;
		  
		res = (n1 + n2 + n3) / 3.0;
		  
		System.out.println("A Média Aritmética dos números 4, 12 e 15 é: " + res);
	  
	}
}